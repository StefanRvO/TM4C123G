#include <stdint.h>
#include "inc/tm4c123gh6pm.h"
#include "headers/setup.h"

int main(void)
/***************************************************************************
* Function : LED follows SWITCH
***************************************************************************/
{
setup();
while(1)
{
if (GPIO_PORTF_DATA_R & 0x10)
GPIO_PORTF_DATA_R &= ~(0x02);
else
GPIO_PORTF_DATA_R |= 0x02;
}
return (0);
}
